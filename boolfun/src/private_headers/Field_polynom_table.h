	bvect32 get_polynom_from_table(int deg)
	{
		bvect32 result = 0;
		for (int i = 0; i < 4; i++)
			set_bit_32(&result, table[deg][i] == 0 ? 0 : table[deg][i] - 1, 1);

		return result |= 1;
	}
